"""Core module."""


def add(x: int, y: int) -> int:
    """
    Add two numbers.

    Args:
        x: First number.
        y: Second number.

    Returns: Result of addition.
    """
    return x + y


def print_hi(name: str) -> None:
    """
    Print hi {user}.

    Args:
        name: User's name.
    """
    print(f"Hi, {name}")
